	adiw	XL,	0x18					;avanzo 0x18 = 24 lugares para el cambio de fila en la memoria de pantalla 
										;(no es 32 porque ya avancé 8 al dibujar la linea). 
	dec		r16
	brne	copia_char1
	pop r0
	pop r1
	ret

