; Para administrar r1 y r0
restar_decena_de_segundo:
dec r1 ; le quito las decenas de segundo
ldi r19, 9
mov r0, r19 ;le pongo el 9 para la siguiente ronda al r0
ori r23, 0b00000011 ;le aviso al tmr0 que ya puede actualizar esos dos números
; reviso, si r1 ya estaba en 0, lo pongo en 5, ya que arranco un nuevo minuto
mov r19, r1
cpi r19, 255
breq adaptar_r1
jmp _tmr1_out

adaptar_r1:
ldi r19, 5
mov r1, r19
jmp _tmr1_out


restar_minuto:
; ya paso un minuto, entonces, debemos bajar r21, debemos cambiar r2, r1, r0 y r22, también revisar si no esta en un borde como con los segundos
dec r21
dec r2 ;decrementamos las unidaades de los minutos
; cargamos los valores en la parte de los segundos
ldi r22, 59
ldi r19, 9
mov r0, r19
ldi r19, 5
mov r1, r19
; habilitamos los cambios
ldi r23, 0b00000111
jmp revisar_minutero




