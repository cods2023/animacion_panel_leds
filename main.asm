; Rutinas para controlar el temporizador
; r21 minutero
; r22 segundero
; r0 más a la derecha
; r1 segúndo, el de la derecha de los dos puntos
; r2 tercero, el primero de la izquierda de los dos puntos
; r3 cuarto, el de más a la izquierda
; 1  1  :  3  4
; r3 r2    r1 r0
revisar_segundero:
cpi r22, 255
breq restar_minuto
cpi r22, 49
breq efectos_50_segundos
cpi r22, 39
breq efectos_40_segundos
cpi r22, 29
breq efectos_30_segundos
cpi r22, 19
breq restar_decena_de_segundo
cpi r22, 9
breq efectos_10_segundos
jmp _tmr1_out

efectos_50_segundos:
jmp efectos_menos_de_un_minuto_segundos

efectos_40_segundos:
jmp efectos_menos_de_un_minuto_segundos

efectos_menos_de_un_minuto_segundos:
cpi r21, 0
brne restar_decena_de_segundo
ldi r19, 7
mov r4, r19
ldi r19, 1
mov r5, r19
mov r6, r19
ori r23, 0b00010000
jmp restar_decena_de_segundo

