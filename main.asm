_tmr1_int:
	push	r16							;guardo contexto: registros y banderas
	in		r16,	SREG

	
	cpi r23, 128 ; si ya llego a 0, usando el registro de control de operaciones (impuesto por nosotros), evitamos que decremente y cambie el valor de r23
	breq _tmr1_out

	; Acá hay que decrementar el segundero
	dec r22 ; el segundero propiamente dicho
	dec r0  ; las unidades de los segundos

	; Para controlar si se cambia o no un valor, usaremos a r23
	; siendo el bit 1 el indicador de que r1 cambio
	; siendo el bit 2 el indicador de que r2 cambio
	; siendo el bit 3 el indicador de que r3 cambio
	; El r0 siempre cambia cuando entra esta rutina, entonces hay que decirle al tmr0 que cambio
	ori r23, 0b00000001

	;Parte del temporizador
	jmp revisar_segundero
	; Fin parte del temporizador
	
	_tmr1_out:
		pop		r16
		out		SREG,	r16
		reti


